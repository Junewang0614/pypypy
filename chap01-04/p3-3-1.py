places = ['usa','fg','jnd','dg','adly']
print('the original list:')
print(places)

print(sorted(places))
print(places)
#利用reverse
places.reverse()
print(places)
places.reverse()
print(places)
print('\n')

#使用sort
places.sort()
print(places)
places.sort(reverse = True)
print(places)

print(len(places))
